// @Summer 发起请求地址
var rateLimiter = time.Tick(1000 * time.Millisecond)

func Fetch(url string) (contents []byte, err error) {
	<-rateLimiter
	rand.Seed(time.Now().UnixNano())
	i := rand.Intn(34)

	client := &http.Client{}
	req, err := http.NewRequest("GET", url, nil)
	if err != nil {
		fmt.Println("上下文请求出错:", err)
		return nil, err
	}
	req.Header.Set("User-Agent", UserAgent[i])
	resp, err := client.Do(req)
	//resp,err := http.Get(url)
	if err != nil || err == io.EOF {
		fmt.Println("请求出错", err)
		return nil, err
	}
	defer resp.Body.Close()
	if resp.StatusCode != http.StatusOK {
		fmt.Println("状态码", resp.StatusCode)
		return nil, err
	}

	bodyReader := bufio.NewReader(resp.Body)
	e := determineEncoding(bodyReader)
	utfReader := transform.NewReader(bodyReader, e.NewDecoder())

	return ioutil.ReadAll(utfReader)
}

func determineEncoding(r *bufio.Reader) encoding.Encoding {
	bytes, err := r.Peek(1024)
	if err != nil {
		return unicode.UTF8
	}
	e, _, _ := charset.DetermineEncoding(bytes, "")
	return e
}
